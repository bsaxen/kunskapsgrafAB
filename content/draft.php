Allt handlar ytterst om information. Det finns ingen verksamhet som inte är beroenda av hur effektivt man hanterar information.
Hanteringen av information innebär att:

- samla in information där den skapas
- integrera med redan skapad information
- representera information utan extra bagage
- använda information där den behövs, t ex applikationer, människor, m m 
- 360 graders vy av informationen, d v s dra nya slutsatser.


Det är viktigt att göra åtskillnad på data och information. Data är olika representationer av information.T ex kan information representeras binärt av 1-or och 0-or.
De utmaningar som är förknippat med hantering av data handlar oftast och lagring och transport. 
